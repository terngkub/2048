#include "game_2048.h"

void	reset_stop(t_map *map)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (i < map->size)
	{
		j = 0;
		while (j < map->size)
			map->stop[i][j++] = 0;
		i++;
	}
}
